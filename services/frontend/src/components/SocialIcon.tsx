export function platformLabel(platform: string): string {
  return BRANDS[platform.toLowerCase()]?.label ?? platform;
}

export function platformColor(platform: string): string {
  return BRANDS[platform.toLowerCase()]?.color ?? "#94a3b8";
}

interface Props {
  platform: string;
  size?: number;
  className?: string;
  style?: CSSProperties;
  /** Override the brand color — defaults to the platform's brand color. */
  color?: string;
  /** When true, sets aria-label on the SVG. Otherwise the parent should label the icon. */
  labelled?: boolean;
}

export function SocialIcon({ platform, size = 16, className, style, color, labelled }: Props) {
  const brand = BRANDS[platform.toLowerCase()];
  if (!brand) {
    // Fallback: first letter in a muted slate.
    return (
      <span
        className={className}
        style={{ fontWeight: 700, fontSize: size * 0.8, lineHeight: 1, color: color ?? "#94a3b8", ...style }}
        aria-label={labelled ? platform : undefined}
      >
        {platform.slice(0, 1).toUpperCase()}
      </span>
    );
  }

  const ariaProps = labelled
    ? { role: "img" as const, "aria-label": brand.label }
    : { "aria-hidden": true };

  return (
    <svg
      {...ariaProps}
      viewBox="0 0 24 24"
      width={size}
      height={size}
      className={className}
      style={{ color: color ?? brand.color, display: "block", ...style }}
      fill="currentColor"
    >
      <path d={brand.path} />
    </svg>
  );
}
